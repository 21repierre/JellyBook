// The purpose of this file is to fix up the rich text so it can be formatted properly

String fixRichText(String text) {
  return text
      .replaceAll("<br>", "\n\t\t\t")
      .replaceAll("<i>", "")
      .replaceAll("</i>", "")
      .replaceAll("<b>", "")
      .replaceAll("</b>", "")
      .replaceAll("<p>", "")
      .replaceAll("</p>", "")
      .replaceAll("<u>", "")
      .replaceAll("</u>", "")
      .replaceAll("<strong>", "")
      .replaceAll("</strong>", "")
      .replaceAll("<em>", "")
      .replaceAll("</em>", "")
      .replaceAll("<a>", "")
      .replaceAll("</a>", "")
      .replaceAll("<ul>", "")
      .replaceAll("</ul>", "")
      .replaceAll("<li>", "")
      .replaceAll("</li>", "")
      .replaceAll("<ol>", "")
      .replaceAll("</ol>", "")
      .replaceAll("<div>", "")
      .replaceAll("</div>", "")
      .replaceAll("<span>", "")
      .replaceAll("</span>", "")
      .replaceAll("<h1>", "")
      .replaceAll("</h1>", "")
      .replaceAll("<h2>", "")
      .replaceAll("</h2>", "")
      .replaceAll("<h3>", "")
      .replaceAll("</h3>", "")
      .replaceAll("<h4>", "")
      .replaceAll("</h4>", "")
      .replaceAll("<h5>", "")
      .replaceAll("</h5>", "")
      .replaceAll("<h6>", "")
      .replaceAll("</h6>", "")
      .replaceAll("<hr>", "")
      .replaceAll("</hr>", "")
      .replaceAll("<blockquote>", "")
      .replaceAll("</blockquote>", "")
      .replaceAll("<pre>", "")
      .replaceAll("</pre>", "")
      .replaceAll("<code>", "")
      .replaceAll("</code>", "")
      .replaceAll("<sup>", "")
      .replaceAll("</sup>", "")
      .replaceAll("<sub>", "")
      .replaceAll("</sub>", "");
}
